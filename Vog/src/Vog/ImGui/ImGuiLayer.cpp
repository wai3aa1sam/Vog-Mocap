	void ImGuiLayer::onEvent(Event& event_)
	{
		EventDispatcher dispatcher(event_);
		if (m_blockEvents)
		{
			ImGuiIO& io = ImGui::GetIO();
			event_.isHandled |= event_.isInCategory(EventCategory::EventCategoryMouse) & io.WantCaptureMouse;
			event_.isHandled |= event_.isInCategory(EventCategory::EventCategoryKey) & io.WantCaptureKeyboard;
		}
	}

